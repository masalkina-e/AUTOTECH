function renderPriceTO() {
    const box = document.querySelector('.service-flex')

    if (box.style.display == "none") {
        const containerPriceMenuMobile = document.getElementById('box-price-mobile1')
        containerPriceMenuMobile.innerHTML = ""

        servicesTO.forEach((service) => {
        containerPriceMenuMobile.innerHTML += `
        <div class="price-menu-item title-price">
            <p>Вид услуг</p>
            <p>Цена (РУБ)</p>
        </div>                   
                                           
        <div class="price-menu-item">
            <p class="price-menu-details">${service.title}</p>
            <p>${service.price}</p>
        </div>
        `
        }) 
    }

    const containerPriceMenu = document.getElementById('box-price')
    containerPriceMenu.innerHTML = ""

    servicesTO.forEach((service) => {
        containerPriceMenu.innerHTML += `
        <div class="price-menu-item">
            <p class="price-menu-details">${service.title}</p>
            <p>${service.price}</p>
        </div>
        `
    }) 

    clearFocusPriceMenu()
    document.getElementById('service-block1').style.backgroundColor = "#f1a66e"
    document.getElementById('price-menu1').style.backgroundColor = "#f1a66e"
}

